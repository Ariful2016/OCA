<h2 style="color:blue">What is College Admission Predictor?</h2>
<h3>Please read the information bellow carefully</h3>
<p>


This project Engineering Admission Predictor System is web based 
application in which students<br> can register with their personal as well as 
marks details for prediction the admission in colleges and the <br>administrator 
can allot the seats for the students. Administrator can add the college <br>
details and he batch details. Using this software, the entrance seat <br>
allotment became easier and can be implemented using system. The main 
advantage of the<br> project is the computerization of the entrance seat 
allotment process.<br> Administrator has the power for the allotment. He can <br>
add the allotted seats into a file and the details are saved into the system. The total time for
<br> the entrance allotment became lesser and the allotment process became 
faster.[1]

<br><br><br>
<b>Features:</b><br>
<p>It helps students to get the list of colleges to which they can apply as the system<br> shortlists the colleges by comparing the student’s marks and college's cut off.<br>
<b>Admin</b> manages all the system related task such as adding college details,<br> specifying cut-off details, managing college details, viewing student’s details and<br> viewing feedback from registered students.<br>
<b>Student</b> can register themselves using web portal by entering their personal as<br> well as marks details. Once registration is successful, they can login and view<br> colleges allotted based on their marks.
</p>
<h4>Advantages:</h4>
<ol>
<li>It helps student for making decision for choosing a right college.</li>
<li>Here the chance of occurrence of error is less when compared with the existing system.</li>
<li>It is fast, efficient and reliable</li>
<li>Avoids data redundancy and inconsistency</li>
<li>Very user-friendly</li>
<li>Easy accessibility of data</li>
<li>Number of personnel required is considerably less</li>
<li>Provides more security and integrity to data.</li>
</ol>


